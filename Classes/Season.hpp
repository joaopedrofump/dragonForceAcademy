#pragma once

#include "Level.h"

class Club;

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	A season. </summary>
///
/// <remarks>	Luís, 20/11/2016. </remarks>
////////////////////////////////////////////////////////////////////////////////////////////////////

class Season {
	/// <summary>	The year. </summary>
	unsigned int year;
	/// <summary>	The initial date. </summary>
	Date initialDate;
	/// <summary>	The end date. </summary>
	Date endDate;
	/// <summary>	The levels. </summary>
	vector<Level*> levels;
	/// <summary>	Pathname of the path to season folder. </summary>
	string pathToSeasonFolder;
	/// <summary>	Name of the season. </summary>
	string seasonName;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Sets date interval. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void setDateInterval();

public:

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Constructor. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <param name="year">		   	The year. </param>
	/// <param name="levelsVector">	[in,out] If non-null, the levels vector. </param>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	Season(unsigned int year, vector<Level*> levelsVector);

	

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Constructor. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <param name="seasonName">	Name of the season. </param>
	/// <param name="parentClub">	[in,out] If non-null, the parent club. </param>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	Season(string seasonName, Club* parentClub);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Gets the year. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <returns>	The year. </returns>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	unsigned int getYear() const;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Gets the levels. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <returns>	Null if it fails, else the levels. </returns>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	vector<Level*> getLevels() const;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Gets path to season folder. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <returns>	The path to season folder. </returns>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	string getPathToSeasonFolder() const;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Gets season name. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <returns>	The season name. </returns>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	string getSeasonName() const;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Gets initial date. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <returns>	The initial date. </returns>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	Date getInitialDate() const;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Gets end date. </summary>
	///
	/// <remarks>	Luís, 20/11/2016. </remarks>
	///
	/// <returns>	The end date. </returns>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	Date getEndDate() const;
};


